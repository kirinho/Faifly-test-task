    NOW() - INTERVAL FLOOR(RAND() * 365) DAY AS start_date_time,
    NOW() - INTERVAL FLOOR(RAND() * 365) DAY + INTERVAL 1 HOUR AS end_date_time,
    FLOOR(RAND() * 100000) + 1 AS patient_id,
